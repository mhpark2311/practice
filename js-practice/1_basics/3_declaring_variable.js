/**
 * Variable 선언
 *
 * 1) var 더이상 쓰지 않음
 * 2) let
 * 3) const
 *
 *
 */

var name = '코드팩토리';
console.log(name);

var age = 32;
console.log(age);

let ive = '아이브';
console.log(ive);

/**
 * let과 var로 선언하면
 * 값을 추후 변경할수 있다
 */

ive = '안유진';
console.log(ive);

const newJeans = '뉴진스';
console.log(newJeans);

// newJeans = '코드팩토';

